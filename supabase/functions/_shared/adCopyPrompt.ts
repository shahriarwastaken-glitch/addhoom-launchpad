export function buildCopyBrief(fields: {
  product: string;
  platform: string;
  language: string;
  tone: string;
  variations: number;
  target_reader?: string;
  awareness_stage?: string;
  sophistication?: string;
  one_idea?: string;
  desires?: string;
  notions?: string;
  identification?: string;
  offer?: string;
  one_action?: string;
}): string {
  return `
TYPE OF COPY: ${fields.platform} ad

PRODUCT OR SERVICE:
${fields.product}

TARGET READER:
${fields.target_reader || 'Not specified — infer from product description'}

STATE OF AWARENESS:
${fields.awareness_stage || 'solution_aware'}

MARKET SOPHISTICATION:
${fields.sophistication || 'medium'}

ONE IDEA:
${fields.one_idea || 'Infer the strongest angle from the product'}

KEY DESIRES TO HIT:
${fields.desires || 'Infer from product and reader description'}

NOTIONS TO RESPECT OR DISMANTLE:
${fields.notions || 'Infer common objections for this product type'}

IDENTIFICATIONS TO LEVERAGE:
${fields.identification || 'Not specified'}

MAIN OBJECTIONS:
Infer from product, reader, and awareness stage.

THE OFFER:
${fields.offer || 'Infer from product description'}

THE ONE ACTION:
${fields.one_action || 'Appropriate CTA for ' + fields.platform}

TONE: ${fields.tone}
LANGUAGE: ${fields.language}
VARIATIONS: ${fields.variations}

Write ${fields.variations} meaningfully
different ad variation(s).
Each must use a different hook angle,
different emotional entry point,
and different framework where possible.
`.trim();
}

export function calculateBriefCompleteness(fields: {
  target_reader?: string;
  awareness_stage?: string;
  sophistication?: string;
  one_idea?: string;
  desires?: string;
  notions?: string;
  identification?: string;
  offer?: string;
  one_action?: string;
}): number {
  const checks = [
    fields.target_reader,
    fields.awareness_stage,
    fields.sophistication,
    fields.one_idea,
    fields.desires,
    fields.notions,
    fields.identification,
    fields.offer,
    fields.one_action,
  ];
  const filled = checks.filter(v => v && v.trim().length > 0).length;
  return Math.round((filled / checks.length) * 100);
}
